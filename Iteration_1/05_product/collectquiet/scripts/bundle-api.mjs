/**
 * Bundle each serverless route into a single ESM file under api/.
 * Avoids Vercel/Node ERR_MODULE_NOT_FOUND on extensionless relative imports
 * when package.json has "type": "module".
 */
import * as esbuild from 'esbuild';
import { readdirSync, statSync, rmSync, mkdirSync } from 'node:fs';
import { join, relative } from 'node:path';

const root = process.cwd();
const serverApi = join(root, 'server', 'api');
const outApi = join(root, 'api');

/** @param {string} dir */
function walk(dir) {
  /** @type {string[]} */
  const out = [];
  for (const name of readdirSync(dir)) {
    const p = join(dir, name);
    if (statSync(p).isDirectory()) out.push(...walk(p));
    else if (name.endsWith('.ts') && !name.endsWith('.d.ts')) out.push(p);
  }
  return out;
}

const entries = walk(serverApi).filter((p) => !p.includes(`${join('api', '_lib')}`) && !p.includes('/_lib/') && !p.includes('\\_lib\\'));

if (!entries.length) {
  console.error('No API route entries found under server/api');
  process.exit(1);
}

rmSync(outApi, { recursive: true, force: true });
mkdirSync(outApi, { recursive: true });

await esbuild.build({
  entryPoints: entries,
  outdir: outApi,
  outbase: serverApi,
  platform: 'node',
  target: 'node20',
  format: 'esm',
  bundle: true,
  // Keep deps in node_modules; only inline workspace TypeScript.
  packages: 'external',
  logLevel: 'info',
  sourcemap: false,
  banner: {
    js: '// Generated by scripts/bundle-api.mjs — do not edit\n',
  },
});

console.log(
  `Bundled ${entries.length} routes → api/:\n` +
    entries.map((e) => `  ${relative(serverApi, e)}`).join('\n')
);
